//构建二叉树
BTnode* BinaryTreeCreate(BTDataType* arr, int size, int *sign)
{
	assert(arr);
	if (size == 0)
	{
		return NULL;
	}
	if (*arr == '#')
	{
		return;
	}
	BTnode* node = (BTnode*)malloc(sizeof(BTnode));
	node->data = *arr;
	node->left = BinaryTreeCreate(arr +1, size);
	node->right = BinaryTreeCreate(arr, size);
	return node;
}

