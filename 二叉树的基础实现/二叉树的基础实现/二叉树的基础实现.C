//查找结点
BTnode* BinaryTreeFindNode(BTnode* root, BTDataType data)
{
	if (root == NULL)
	{
		return NULL;
	}
	if (root->data == data)
	{
		return root;
	}
	BTnode* node = BinaryTreeFindNode(root->left, data);
	if (node == NULL)
	{
		BTnode* node2 = BinaryTreeFindNode(root->right, data);
		if (node2 == NULL)
		{
			return NULL;
		}
		else
		{
			return node2;
		}
	}
	else
	{
		return node;
	}
}